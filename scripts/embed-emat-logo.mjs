import { readFileSync, writeFileSync } from "fs";
import { fileURLToPath } from "url";
import { dirname, join } from "path";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const pngPath = join(root, "public/emat-logo.png");
const outPath = join(root, "src/lib/ematLogoData.js");

const base64 = readFileSync(pngPath).toString("base64");
const dataUri = `data:image/png;base64,${base64}`;

const content = `// Auto-generated by scripts/embed-emat-logo.mjs — do not edit manually.
export const EMAT_LOGO_DATA_URI = ${JSON.stringify(dataUri)};
export const EMAT_LOGO_BASE64 = ${JSON.stringify(base64)};
`;

writeFileSync(outPath, content);
console.log(`Wrote ${outPath} (${base64.length} chars base64)`);
